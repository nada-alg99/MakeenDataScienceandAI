
#Calculator

first = input("Enter the first number:  ")
operator = input("The operator: ")
second = input("Enter the second number:  ")

if operator == "*":
    print("the answer is", float(first) * float(second))
    
elif operator == "-":
    print("the answer is", float(first) - float(second))
    
elif operator == "+":
    print("the answer is", float(first) + float(second))
    
elif operator == "/":
    print("the answer is", float(first) / float(second))
    


    

    

    






